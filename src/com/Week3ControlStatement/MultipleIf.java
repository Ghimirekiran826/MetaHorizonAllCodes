package com.Week3ControlStatement;

public class MultipleIf {
	public static void main(String[] args) {
		int firstnum;
		firstnum =18;
		if(firstnum==0)
		{
			System.out.println("Zero");
		}
		if(firstnum!=0)
		{
			System.out.println("Non Zero");
		}
		if(!(firstnum!=0))
		{
			System.out.println("Non Zero");
		}
	}
}
